# Logistic Regression
"""
NOTES

The logistic regression coge la variable independiente y aplica una especie de 
funcion sigmoid para darnos la probabilidad de que, basandosonos en dicha variable
algo sea verdadero o no (1 o 0):
    
ln(p/(1-p))=b0+b1*x

Cualquier nomenclatura con un sombrerito, se trata de una prediccion.

"""


# LIBRARIES + DATASET
import numpy as np
import matplotlib.pyplot as plt
import pandas as pd

# Importing the dataset
dataset = pd.read_csv('Social_Network_Ads.csv')
X = dataset.iloc[:, [2, 3]].values
y = dataset.iloc[:, 4].values

from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size = 0.25, random_state = 0)

# Feature Scaling
from sklearn.preprocessing import StandardScaler
sc = StandardScaler()
X_train = sc.fit_transform(X_train)
X_test = sc.transform(X_test)



# HERE STARTS THE ALGORITHM
# Fitting Logistic Regression to the Training set
from sklearn.linear_model import LogisticRegression
classifier = LogisticRegression(random_state = 0)
classifier.fit(X_train, y_train)

# Predicting the Test set results
y_pred = classifier.predict(X_test)



# EVALUATION OF THE PERFOMANCE OF THIS LOGISTIC REGRESSION
# Making the Confusion Matrix
"""
Usamos la confusion Matrix. Â¿Que es? Es una matriz cuadrada de dimensiones nxn
siendo n el numero de posibles valores que y puede tomar (asumimos que solo un
numero concreto de valores ya que estamos en un problema de clasificacion). 

Los elementdos diagonales son aquellos casos que al comparar el resultado de la y_pred
con la y_test=y_real coinciden. Si se ha clasificado como otro resultado de y,
se suma a una de las otras columnas. 

Por tanto, buscamos maximizar el valor
de los elementos de la diagonal de la matriz. La matriz tambien puede servir para
determinar sesgos estructural que pueden existir a la hora de clasificar resultados 
un resultado en concreto de todos los posibles.
"""
from sklearn.metrics import confusion_matrix
cm = confusion_matrix(y_test, y_pred)

# Visualising the Training set results
from matplotlib.colors import ListedColormap
X_set, y_set = X_train, y_train
X1, X2 = np.meshgrid(np.arange(start = X_set[:, 0].min() - 1, stop = X_set[:, 0].max() + 1, step = 0.01),
                     np.arange(start = X_set[:, 1].min() - 1, stop = X_set[:, 1].max() + 1, step = 0.01))
plt.contourf(X1, X2, classifier.predict(np.array([X1.ravel(), X2.ravel()]).T).reshape(X1.shape),
             alpha = 0.75, cmap = ListedColormap(('red', 'green')))
plt.xlim(X1.min(), X1.max())
plt.ylim(X2.min(), X2.max())

for i, j in enumerate(np.unique(y_set)):
    plt.scatter(X_set[y_set == j, 0], X_set[y_set == j, 1],
                c = ListedColormap(('red', 'green'))(i), label = j)
    
plt.title('Logistic Regression (Training set)')
plt.xlabel('Age')
plt.ylabel('Estimated Salary')
plt.legend()
plt.show()

